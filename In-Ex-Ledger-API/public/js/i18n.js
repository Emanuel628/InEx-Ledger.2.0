const LANGUAGES = ['en', 'es', 'fr'];

const LANGUAGE_LABELS = {
  en: 'English',
  es: 'Espa\u00F1ol',
  fr: 'Fran\u00E7ais'
};

function normalizeLanguage(code) {
  if (!code || typeof code !== 'string') {
    return 'en';
  }

  const normalized = code.toLowerCase();
  return LANGUAGES.includes(normalized) ? normalized : 'en';
}

function normalizeRegion(code) {
  if (!code || typeof code !== 'string') {
    return 'us';
  }

  const normalized = code.trim().toLowerCase();
  if (normalized === 'ca' || normalized === 'canada') {
    return 'ca';
  }
  if (normalized === 'us' || normalized === 'usa' || normalized === 'united states' || normalized === 'united states of america') {
    return 'us';
  }
  return 'us';
}

function getStoredLanguage() {
  return normalizeLanguage(localStorage.getItem('lb_language'));
}

function getCurrentLanguage() {
  return window.LUNA_LANGUAGE || getStoredLanguage();
}

function getCurrentRegion() {
  return normalizeRegion(window.LUNA_REGION || localStorage.getItem('lb_region'));
}

function getCurrentProvince() {
  return String(window.LUNA_PROVINCE || localStorage.getItem('lb_province') || '').toUpperCase();
}

function setCurrentProvince(province) {
  const normalized = String(province || '').toUpperCase();
  localStorage.setItem('lb_province', normalized);
  window.LUNA_PROVINCE = normalized;
  return normalized;
}

function setCurrentLanguage(lang) {
  const normalized = normalizeLanguage(lang);
  localStorage.setItem('lb_language', normalized);
  window.LUNA_LANGUAGE = normalized;
  applyTranslations(normalized);
  console.log('[i18n] language:', normalized);
  return normalized;
}

function setCurrentRegion(region, province) {
  const normalized = normalizeRegion(region);
  localStorage.setItem('lb_region', normalized);
  window.LUNA_REGION = normalized;
  if (province !== undefined) {
    setCurrentProvince(province);
  }
  const resolvedProvince = getCurrentProvince();
  applyTranslations(getCurrentLanguage());
  applyRegionHardening(normalized, resolvedProvince);
  if (typeof window !== 'undefined' && typeof CustomEvent === 'function') {
    window.dispatchEvent(
      new CustomEvent('lunaRegionChanged', { detail: normalized })
    );
  }
  return normalized;
}

/**
 * applyRegionHardening — shows/hides DOM elements based on data-region-show
 * and data-region-hide attributes using the current user region/province.
 *
 * data-region-show="us"    → visible only for US users
 * data-region-show="ca"    → visible only for CA users
 * data-region-show="ca-qc" → visible only for Quebec users (CA + province QC)
 * data-region-hide="us"    → hidden for US users
 * data-region-hide="ca"    → hidden for CA users
 * data-region-hide="ca-qc" → hidden for Quebec users
 */
function applyRegionHardening(regionOverride, provinceOverride) {
  const region = normalizeRegion(regionOverride !== undefined ? regionOverride : getCurrentRegion());
  const province = String(provinceOverride !== undefined ? provinceOverride : getCurrentProvince()).toUpperCase();
  const isCA = region === 'ca';
  const isUS = region === 'us';
  const isQC = isCA && province === 'QC';

  function matchesTarget(target) {
    const t = String(target || '').toLowerCase().trim();
    if (t === 'us') return isUS;
    if (t === 'ca') return isCA;
    if (t === 'ca-qc') return isQC;
    return false;
  }

  document.querySelectorAll('[data-region-show]').forEach(function (node) {
    const target = node.getAttribute('data-region-show');
    const visible = matchesTarget(target);
    node.classList.toggle('region-hidden', !visible);
  });

  document.querySelectorAll('[data-region-hide]').forEach(function (node) {
    const target = node.getAttribute('data-region-hide');
    const hidden = matchesTarget(target);
    node.classList.toggle('region-hidden', hidden);
  });

  // Dispatch event so pages can react
  if (typeof window !== 'undefined' && typeof CustomEvent === 'function') {
    window.dispatchEvent(new CustomEvent('lunaRegionHardened', {
      detail: { region: region, province: province, isCA: isCA, isUS: isUS, isQC: isQC }
    }));
  }
}

function t(key) {
  const lang = getCurrentLanguage();
  const translations = TRANSLATIONS[lang] || TRANSLATIONS.en || {};
  return translations[key] || (TRANSLATIONS.en && TRANSLATIONS.en[key]) || key;
}

function setTranslatedContent(node, text) {
  if (!node) {
    return;
  }

  if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA') {
    node.value = text;
    return;
  }

  const brandText = node.querySelector('.topbar-brand-text');
  if (brandText) {
    brandText.textContent = text;
    return;
  }

  if (node.querySelector('.nav-icon')) {
    const labelTarget = Array.from(node.children).find((child) =>
      !child.classList.contains('nav-icon') && !child.classList.contains('brand-mark')
    );
    if (labelTarget) {
      labelTarget.textContent = text;
      return;
    }
  }

  node.textContent = text;
}

function applyTranslations(languageOverride) {
  const language = normalizeLanguage(languageOverride || getCurrentLanguage());
  const region = getCurrentRegion();
  window.LUNA_LANGUAGE = language;
  localStorage.setItem('lb_language', language);

  // Update the document language attribute so screen readers announce the
  // correct language when the user switches between en / es / fr.
  if (document.documentElement) {
    document.documentElement.lang = language;
  }

  const nodes = document.querySelectorAll('[data-i18n]');
  nodes.forEach((node) => {
    if (node.id === 'passwordMatchMessage') {
      return;
    }
    const key = node.getAttribute('data-i18n');
    if (!key) return;
    const text = t(key);
    setTranslatedContent(node, text);
  });

  const regionNodes = document.querySelectorAll('[data-i18n-region]');
  regionNodes.forEach((node) => {
    const baseKey = node.getAttribute('data-i18n-region');
    if (!baseKey) return;
    const regionKey = `${baseKey}_${region}`;
    const text = t(regionKey) !== regionKey ? t(regionKey) : t(baseKey);
    if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA') {
      node.value = text;
    } else {
      node.textContent = text;
    }
  });

  const placeholderNodes = document.querySelectorAll('[data-i18n-placeholder]');
  placeholderNodes.forEach((node) => {
    const key = node.getAttribute('data-i18n-placeholder');
    if (!key) return;
    const text = t(key);
    if (typeof node.placeholder !== 'undefined') {
      node.placeholder = text;
    }
  });

  const ariaLabelNodes = document.querySelectorAll('[data-i18n-aria-label]');
  ariaLabelNodes.forEach((node) => {
    const key = node.getAttribute('data-i18n-aria-label');
    if (!key) return;
    node.setAttribute('aria-label', t(key));
  });

  const titleNodes = document.querySelectorAll('[data-i18n-title]');
  titleNodes.forEach((node) => {
    const key = node.getAttribute('data-i18n-title');
    if (!key) return;
    node.setAttribute('title', t(key));
  });

  console.log('[i18n] language:', language);
  if (typeof window !== 'undefined' && typeof CustomEvent === 'function') {
    window.dispatchEvent(
      new CustomEvent('lunaLanguageChanged', { detail: language })
    );
  }
  // Apply region-based element visibility after updating text
  applyRegionHardening();
}

function populateLanguageOptions(selectElement) {
  if (!selectElement) return;

  const existingOptions = new Map();
  Array.from(selectElement.options).forEach((option) => {
    existingOptions.set(option.value, option);
  });

  LANGUAGES.forEach((code) => {
    let option = existingOptions.get(code);
    if (!option) {
      option = document.createElement('option');
      option.value = code;
      selectElement.appendChild(option);
    }
    option.textContent = LANGUAGE_LABELS[code] || code;
    option.selected = code === getCurrentLanguage();
    existingOptions.delete(code);
  });

  existingOptions.forEach((option) => option.remove());
}

